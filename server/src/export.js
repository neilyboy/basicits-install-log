async function serveReportInline(jobId, res, shareUrl, options = {}) {
  const jobData = getFullJobData(jobId);
  if (!jobData) {
    return res.status(404).send('<html><body style="font-family:sans-serif;padding:2rem"><h2>Job not found</h2></body></html>');
  }
  const imageDataMap = loadImageDataMap(jobData);
  const html = await generateReportHtml(jobData, imageDataMap, {
    shareUrl: shareUrl || null,
    isLive: true,
    autoPrint: options.autoPrint || false,
  });
  res.setHeader('Content-Type', 'text/html; charset=utf-8');
  res.setHeader('X-Content-Type-Options', 'nosniff');
  res.send(html);
}

async function exportJobAsZip(jobId, res, options = {}) {
  const jobData = getFullJobData(jobId);
  if (!jobData) throw new Error('Job not found');

  const imageDataMap = loadImageDataMap(jobData);
  const safeJobName = jobData.name.replace(/[^a-z0-9_\-\s]/gi, '_').replace(/\s+/g, '_').toLowerCase();

  res.setHeader('Content-Type', 'application/zip');
  res.setHeader('Content-Disposition', `attachment; filename="basicits_${safeJobName}_${Date.now()}.zip"`);

  const archive = archiver('zip', { zlib: { level: 6 } });
  archive.pipe(res);

  const reportHtml = await generateReportHtml(jobData, imageDataMap, {
    shareUrl: options.shareUrl || null,
    isLive: false,
  });
  archive.append(reportHtml, { name: 'report.html' });
  archive.append(generateCsv(jobData), { name: 'devices.csv' });
  archive.append(JSON.stringify(jobData, null, 2), { name: 'data.json' });
  archive.append(generateReadme(jobData, options.shareUrl || null), { name: 'README.md' });

  for (const photo of (jobData.devices || []).flatMap(d => d.photos || [])) {
    const fp = path.join(UPLOADS_DIR, photo.filename);
    if (fs.existsSync(fp)) archive.file(fp, { name: `images/${photo.filename}` });
  }

  await archive.finalize();
}

