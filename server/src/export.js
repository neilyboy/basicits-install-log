function getFullJobData(jobId) {
  const job = db.prepare('SELECT * FROM jobs WHERE id = ?').get(jobId);
  if (!job) return null;
  const devices = db.prepare(`
    SELECT d.*, hm.model_name, hm.model_number, hm.category as hw_category, hm.brand, hm.description as hw_description
    FROM devices d
    LEFT JOIN hardware_models hm ON d.hardware_model_id = hm.id
    WHERE d.job_id = ?
    ORDER BY d.sort_order ASC, d.created_at ASC
  `).all(jobId);
  const deviceIds = devices.map(d => d.id);
  let photos = [];
  if (deviceIds.length > 0) {
    photos = db.prepare(
      `SELECT * FROM photos WHERE device_id IN (${deviceIds.map(() => '?').join(',')}) ORDER BY sort_order ASC, created_at ASC`
    ).all(...deviceIds);
  }
  return { ...job, devices: devices.map(d => ({ ...d, photos: photos.filter(p => p.device_id === d.id) })) };
}

function loadImageDataMap(jobData) {
  const map = {};
  for (const photo of (jobData.devices || []).flatMap(d => d.photos || [])) {
    const fp = path.join(UPLOADS_DIR, photo.filename);
    if (fs.existsSync(fp)) {
      const buf = fs.readFileSync(fp);
      const ext = path.extname(photo.filename).slice(1).toLowerCase() || 'jpg';
      const mime = ext === 'png' ? 'image/png' : ext === 'webp' ? 'image/webp' : 'image/jpeg';
      map[photo.filename] = `data:${mime};base64,${buf.toString('base64')}`;
    }
  }
  return map;
}

