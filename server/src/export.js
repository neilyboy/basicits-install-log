function generateCsv(jobData) {
  const q = s => s ? `"${String(s).replace(/"/g, '""')}"` : '""';
  const rows = [['#','Device Name','Category','Brand','Model','Model Number','Location','Notes','Photos'].join(',')];
  (jobData.devices || []).forEach((d, i) => {
    rows.push([
      i + 1, q(d.name),
      q(d.hw_category || d.device_type || ''),
      q(d.brand || 'Verkada'),
      q(d.model_name || ''),
      q(d.model_number || ''),
      q(d.location || ''),
      q(d.notes || ''),
      (d.photos || []).length,
    ].join(','));
  });
  return rows.join('\r\n');
}

