function deviceCardHtml(device, di, imageDataMap) {
  const thumbs = (device.photos || []).map((photo, pi) => {
    const src = imageDataMap[photo.filename];
    if (!src) return '';
    return `<div class="photo-thumb" onclick="lb(${di},${pi})">
        <img src="${src}" alt="${H(photo.caption || 'Photo ' + (pi + 1))}" loading="lazy"/>
        ${photo.caption ? `<p class="thumb-cap">${H(photo.caption)}</p>` : ''}
      </div>`;
  }).join('');

  const lbData = JSON.stringify((device.photos || []).map(p => ({
    src: imageDataMap[p.filename] || '',
    caption: p.caption || '',
  }))).replace(/'/g, '&#39;');

  return `
<div class="device-card avoid-break" id="device-${di}">
  <div class="dcard-header">
    <span class="dcat-icon">${CAT_ICON[device.hw_category || device.device_type] || '📦'}</span>
    <div class="dcard-title">
      <h3>${H(device.name)}</h3>
      ${device.model_name ? `<span class="dmodel-tag">${H(device.brand || 'Verkada')} ${H(device.model_name)}${device.model_number ? ` &middot; <code>${H(device.model_number)}</code>` : ''}</span>` : ''}
    </div>
    <span class="dnum-badge">#${di + 1}</span>
  </div>
  ${device.location ? `<div class="dmeta-row"><span class="dloc-pill">📍 ${H(device.location)}</span></div>` : ''}
  ${device.notes ? `<div class="dnotes">${H(device.notes)}</div>` : ''}
  ${thumbs
    ? `<div class="photos-grid" data-device="${di}" data-photos='${lbData}'>${thumbs}</div>`
    : '<p class="no-photos">No photos recorded for this device.</p>'}
</div>`;
}

